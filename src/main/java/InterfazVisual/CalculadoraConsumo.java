    private void LimpiarBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LimpiarBTNActionPerformed
    // 1. Limpiar los campos de entrada (Izquierda)
    Cantidad.setText("");
    Potencia.setText("");
    Dispositivos.setSelectedIndex(0); // Regresa el ComboBox al primer elemento

    // 2. Limpiar la tabla (Borra todas las filas)
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) Carga.getModel();
    modelo.setRowCount(0);

    // 3. Limpiar los campos de resultados (Derecha)
    ConsumoT.setText("");
    FactorS.setText("");
    Capacidad.setText("");

    // 4. Opcional: Poner el foco en el primer campo para empezar de nuevo
    Cantidad.requestFocus();
    }//GEN-LAST:event_LimpiarBTNActionPerformed

