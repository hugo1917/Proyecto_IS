    private void AgregarBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarBTNActionPerformed
    try {
        String nombre = Dispositivos.getSelectedItem().toString();
        int cant = Integer.parseInt(Cantidad.getText());
        double pot = Double.parseDouble(Potencia.getText());
        double totalRow = cant * pot;

        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) Carga.getModel();
        modelo.addRow(new Object[]{nombre, cant, totalRow});

        Cantidad.setText("");
        Potencia.setText("");
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: Por favor, ingresa números válidos.");
    }
    }//GEN-LAST:event_AgregarBTNActionPerformed

    private void CalcularBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularBTNActionPerformed
    double sumaTotalWatts = 0;
    int filas = Carga.getRowCount();

    for (int i = 0; i < filas; i++) {
        sumaTotalWatts += Double.parseDouble(Carga.getValueAt(i, 2).toString());
    }

    double factorSeguridad = sumaTotalWatts * 0.20; 
    double capacidadUPS = (sumaTotalWatts + factorSeguridad) / 0.7; 

    ConsumoT.setText(String.format("%.2f W", sumaTotalWatts));
    FactorS.setText(String.format("%.2f W", factorSeguridad));
    Capacidad.setText(String.format("%.0f VA", capacidadUPS));
    }//GEN-LAST:event_CalcularBTNActionPerformed

    private void DispositivosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DispositivosActionPerformed
    String seleccion = Dispositivos.getSelectedItem().toString();
    
    if (seleccion.equals("Router")) {
        Potencia.setText("150");
    } else if (seleccion.equals("Switch")) {
        Potencia.setText("300");
    } else if (seleccion.equals("Servidor DELL")) {
        Potencia.setText("750");
    } else if (seleccion.equals("Punto de Acceso")) {
        Potencia.setText("30");
    } else if (seleccion.equals("Telefonia IP")) {
        Potencia.setText("15");
    } 
    }//GEN-LAST:event_DispositivosActionPerformed

